    private void PExcluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_PExcluirMouseClicked
        // TODO add your handling code here:
        int linha = tVeiculo.getSelectedRow();
        if(linha != -1){
            String chassis = tVeiculo.getValueAt(0, linha).toString();
            ControleVeiculo controle = new ControleVeiculo();
            Veiculo v = controle.findByChassis(chassis);
            v.setStatus(false);
            try {
                controle.alterar(v);
                JOptionPane.showMessageDialog(null, "Veiculo excluído com sucesso!");
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Erro ao excluir o veiculo!");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Selecione um veiculo!");
        }
    }//GEN-LAST:event_PExcluirMouseClicked

