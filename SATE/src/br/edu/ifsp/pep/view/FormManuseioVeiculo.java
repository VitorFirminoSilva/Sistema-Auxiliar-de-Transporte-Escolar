    private void botPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botPesquisarActionPerformed
        // TODO add your handling code here:

        ControleVeiculo controle = new ControleVeiculo();
        List<Veiculo> list;
        DefaultTableModel model = (DefaultTableModel) tVeiculo.getModel();
        model.setNumRows(0);
        list = controle.findByModelo(txtPesquisar.getText());
        for(Veiculo v: list){
            model.addRow(new Object[] {v.getChassis(), v.getModelo(), v.getPlaca()});
        }
    }//GEN-LAST:event_botPesquisarActionPerformed

