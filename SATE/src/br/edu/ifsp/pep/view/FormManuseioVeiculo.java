    private void PRelatorioManutencoesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_PRelatorioManutencoesMouseClicked
        // TODO add your handling code here:
        int linha = tVeiculo.getSelectedRow();
        if(linha != -1){
            
  
            EntityManager em = Persistence.createEntityManagerFactory("ProjetoIntegradoPU").createEntityManager();

            try {
                JasperDesign jd = JRXmlLoader
                  .load(getClass()
                  .getResourceAsStream("/br/edu/ifsp/pep/report/Veiculo.jrxml"));

                JasperReport relatorioCompilado
                        = JasperCompileManager
                                .compileReport(jd);
                HashMap<String, Object> parametros = new HashMap<>();
                parametros.put(JRJpaQueryExecuterFactory.PARAMETER_JPA_ENTITY_MANAGER,em);
                
  
                for(int i=0; i<3; i++){
                    if(tVeiculo.getColumnName(i).equals("Chassis")){
                        parametros.put("chassis",  tVeiculo.getValueAt(linha, i).toString());
                    }               
                }

                BufferedImage bi = null;
                try {
                    bi = ImageIO.read(getClass().getResource("/br/edu/ifsp/pep/report/logo.PNG"));
                } catch (IOException ex) {
                    System.out.println("Erro ao carregar imagem.");
                }

                parametros.put("logo", bi);

                JasperPrint jp = JasperFillManager.fillReport(relatorioCompilado, parametros);

                JasperViewer viewer = new JasperViewer(jp, false);
                viewer.setVisible(true);
            } catch (JRException ex) {
                Logger.getLogger(FormManuseioVeiculo.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            JOptionPane.showMessageDialog(null, "selecione um veiculo!");
        }
        this.dispose();
    }//GEN-LAST:event_PRelatorioManutencoesMouseClicked

