    private void pSalvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_pSalvarMouseClicked
        // TODO add your handling code here:

        Veiculo veiculo = new Veiculo();
        ControleVeiculo controle = new ControleVeiculo();

        if (txtChassis.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha o campo N° Registro!");
            txtChassis.requestFocus();
            return;
        }
        if (txtModelo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha o campo Nome!");
            txtModelo.requestFocus();
            return;
        }

        if (txtQtdAssentos.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha o campo Naturalidade!");
            txtQtdAssentos.requestFocus();
            return;
        }
        if (txtCondicao.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha o campo Nome Pai!");
            txtCondicao.requestFocus();
            return;
        }


        veiculo.setChassis(txtChassis.getText());
        veiculo.setModelo(txtModelo.getText());
        veiculo.setQtdAssentos(Integer.parseInt(txtQtdAssentos.getText()));
        veiculo.setCondicao(txtCondicao.getText());
        veiculo.setPlaca(txtPlaca.getText());
        veiculo.setQuilometragem(Integer.parseInt(txtQuilometragem.getText()));
        veiculo.setStatus((cStatus.getSelectedIndex() == 0));


        if(this.veiculo==null){
            try {
                controle.inserir(veiculo);
                JOptionPane.showMessageDialog(null, "Veiculo cadastrado com sucesso!");
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Erro ao cadastrar veiculo!");
            }
        }else{
            veiculo.setChassis(this.veiculo.getChassis());
            try {
                controle.alterar(veiculo);
                JOptionPane.showMessageDialog(null, "Veiculo alterado com sucesso!");
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Erro ao alterar veiculo!");
            }
        }

        //FormManuseioVeiculo tela = new FormManuseioVeiculo(null, false);
        //tela.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_pSalvarMouseClicked

