    private void PExcluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_PExcluirMouseClicked
        // TODO add your handling code here:
        int linha = tManutencao.getSelectedRow();
        Integer codigo = 0;
        ControleManutencao controle = new ControleManutencao();
        ControleVeiculo contV = new ControleVeiculo();
        if(linha != -1){
            for(int i=0; i<6; i++){
                System.out.println();
                if(tManutencao.getColumnName(i).equals("Código")){
                    codigo = Integer.parseInt(tManutencao.getValueAt(linha, i).toString());
                }               
            }          
            try{
                Manutencao manu = controle.findByCodigo(codigo, veiculoSelecionado.getChassis());
                if(manu.getStatus().equals("PD")){
                    try {
                        
                        List<Manutencao> lista = veiculoSelecionado.getListaManutencoes();
                        
                        lista.remove(manu);
                        controle.deletar(manu);
                        veiculoSelecionado.setListaManutencoes(lista);
                        contV.alterar(veiculoSelecionado);
                        JOptionPane.showMessageDialog(null, "Manutenção excluída com sucesso!");
                    } catch (Exception ex) {
                        JOptionPane.showMessageDialog(null, "Erro ao excluir Manutenção!");
                    }
                }
                else{
                    JOptionPane.showMessageDialog(null, "Manutenções pagas não podem ser excluidas!");
                }

            }catch(NoResultException ex){
               JOptionPane.showMessageDialog(null, "Erro veiculo não encontrado!"); 
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Selecione uma Manutenção!");
        }
        
        botPesquisarMouseClicked(evt);
    }//GEN-LAST:event_PExcluirMouseClicked

