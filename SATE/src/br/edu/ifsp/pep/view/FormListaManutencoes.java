    private void botPesquisarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botPesquisarMouseClicked
        // TODO add your handling code here:
        ControleManutencao controle = new ControleManutencao();
        ControleVeiculo cv = new ControleVeiculo();
        List<Manutencao> list = new ArrayList<>();
        DefaultTableModel model = (DefaultTableModel) tManutencao.getModel();
        try {
            veiculoSelecionado = cv.findByChassis(veiculoSelecionado.getChassis());
            
            model.setNumRows(0);
            list = controle.findByChassis(veiculoSelecionado.getChassis());
            for(Manutencao m: list){
              model.addRow(new Object[] {m.getCodigo(),Conversoes.getDateFormatedToString(m.getDataManutencao()), m.getTipoManutencao(), m.getVeiculo().getPlaca(), m.getValor(), m.getStatus()});
            }
        } catch (NoResultException ex) {
            JOptionPane.showMessageDialog(null, "Veiculo não encontrado!");
        }
    }//GEN-LAST:event_botPesquisarMouseClicked

