    private void PAlterarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_PAlterarMouseClicked
        int linha = tCliente.getSelectedRow();
        
        if(linha != -1){        
            ControleCliente controle = new ControleCliente();
            String CPF = tCliente.getValueAt(0, linha).toString();
            clienteSelecionado = controle.findByCpf(CPF);
            ClienteForm tela = new ClienteForm(null,true);
            tela.setCliente(clienteSelecionado);
            tela.setVisible(true);
            //this.dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Selecione um Cliente!");
        }
    }//GEN-LAST:event_PAlterarMouseClicked

