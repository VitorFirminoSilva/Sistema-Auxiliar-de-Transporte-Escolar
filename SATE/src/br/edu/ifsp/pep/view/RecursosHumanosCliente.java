    private void botPesquisarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botPesquisarMouseClicked
        // TODO add your handling code here:
        
        ControleCliente controle = new ControleCliente();
        List<Cliente> list;
        DefaultTableModel model = (DefaultTableModel) tCliente.getModel();
        model.setNumRows(0);
        list = controle.findByNome(txtPesquisar.getText());
        for(Cliente c: list){
            model.addRow(new Object[] {c.getCpfResponsavel(), c.getNomeMae(), c.getTelefone()});
        }
    }//GEN-LAST:event_botPesquisarMouseClicked

    private void PRelatorioClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_PRelatorioClientesMouseClicked
        // TODO add your handling code here:
        EntityManager em = Persistence.createEntityManagerFactory("ProjetoIntegradoPU").createEntityManager();

        try {
            JasperDesign jd = JRXmlLoader
              .load(getClass()
              .getResourceAsStream("/br/edu/ifsp/pep/report/Clientes.jrxml"));

            JasperReport relatorioCompilado
                    = JasperCompileManager
                            .compileReport(jd);
            HashMap<String, Object> parametros = new HashMap<>();
            parametros.put(JRJpaQueryExecuterFactory.PARAMETER_JPA_ENTITY_MANAGER,em);

            Calendar calen = Calendar.getInstance();
            Calendar calen2 = Calendar.getInstance();
            calen2.set(calen2.get(Calendar.YEAR),calen2.get(Calendar.MONTH), 01);

            
            parametros.put("dataInicio", Conversoes.getDateOfString(Conversoes.getDateFormatedToString(calen2.getTime())));
            // Data inicial sempre referente ao m√™s atual no caso o dia 1  
            parametros.put("dataFim", Conversoes.getDateOfString(Conversoes.getDateFormatedToString(calen.getTime())));
            // Data final √© definida com a data atual da pesquisa, sendo que pode n√£o haver¬¥parcelas no relat√≥rio 


            
            BufferedImage bi = null;
            try {
                bi = ImageIO.read(getClass().getResource("/br/edu/ifsp/pep/report/logo.PNG"));
            } catch (IOException ex) {
                System.out.println("Erro ao carregar imagem.");
            }

            parametros.put("logo", bi);

            JasperPrint jp = JasperFillManager.fillReport(relatorioCompilado, parametros);

            JasperViewer viewer = new JasperViewer(jp, false);
            viewer.setVisible(true);
        } catch (JRException ex) {
            Logger.getLogger(RecursosHumanosCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.dispose();
    }//GEN-LAST:event_PRelatorioClientesMouseClicked

