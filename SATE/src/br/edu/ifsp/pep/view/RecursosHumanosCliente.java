    private void PExcluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_PExcluirMouseClicked
        int linha = tCliente.getSelectedRow();
        String cpf = "";
        if(linha != -1){
            for(int i=0; i<3; i++){
                if(tCliente.getColumnName(i).equals("CPF")){
                    cpf = tCliente.getValueAt(linha, i).toString();
                }               
            }          

            ControleCliente controle = new ControleCliente(); 
            Cliente a = controle.findByCpf(cpf);
            a.setStatus(false);
            try {
                controle.alterar(a);
                JOptionPane.showMessageDialog(null, "Cliente exclu√≠do com sucesso!");
                botPesquisarMouseClicked(evt);
            } catch (Exception ex) {
                Logger.getLogger(RecursosHumanosMotorista.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Erro ao excluir o Cliente!");
            }        
        }
        else{
            JOptionPane.showMessageDialog(null, "Selecione um cliente");
        }
    }//GEN-LAST:event_PExcluirMouseClicked

