    private void pAlterarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_pAlterarMouseClicked
        ControleMotorista controle = new ControleMotorista();
        Motorista motorista = new Motorista();
        int linha = tMotorista.getSelectedRow();
        if(linha != -1){            
            motorista = controle.findByNumRegistro(tMotorista.getValueAt(0, linha).toString());
            MotoristaForm tela = new MotoristaForm(null,true);
            tela.setMotorista(motorista);
            tela.setVisible(true);
            
        }else{
            JOptionPane.showMessageDialog(null, "selecione um motorista!");
        }
    }//GEN-LAST:event_pAlterarMouseClicked

