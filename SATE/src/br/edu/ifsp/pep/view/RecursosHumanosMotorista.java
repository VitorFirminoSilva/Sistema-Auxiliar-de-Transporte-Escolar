    private void PExcluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_PExcluirMouseClicked
        // TODO add your handling code here:
        int linha = tMotorista.getSelectedRow();
        if(linha != -1){
            String numeroRegistro = tMotorista.getValueAt(0, linha).toString();
            ControleMotorista controle = new ControleMotorista();
            Motorista m = controle.findByNumRegistro(numeroRegistro);
            m.setStatus(false);
            try {
                controle.alterar(m);
                JOptionPane.showMessageDialog(null, "Motorista excluÃ­do com sucesso!");
                botPesquisarMouseClicked(evt);
            } catch (Exception ex) {
                Logger.getLogger(RecursosHumanosMotorista.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Erro ao excluir o motorista!");
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Selecione um motorista");
        }
    }//GEN-LAST:event_PExcluirMouseClicked

