    private void botPesquisarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botPesquisarMouseClicked
        // TODO add your handling code here:
        ControleAdministrador controle = new ControleAdministrador();
        List<Administrador> list = new ArrayList<>();
        DefaultTableModel model = (DefaultTableModel) tAdministrador.getModel();
        model.setNumRows(0);
        list = controle.findByNome(txtPesquisar.getText());
        for(Administrador a: list){
            model.addRow(new Object[] {a.getNumeroRegistro(), a.getNomeFuncionario(), a.getSexo()});
        }       
    }//GEN-LAST:event_botPesquisarMouseClicked

