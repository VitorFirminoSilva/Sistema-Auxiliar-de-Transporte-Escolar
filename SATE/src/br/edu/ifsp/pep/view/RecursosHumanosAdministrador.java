    private void PAlterarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_PAlterarMouseClicked
        int linha = tAdministrador.getSelectedRow();
        
        if(linha != -1){        
            ControleAdministrador controle = new ControleAdministrador();
            String numregistro = tAdministrador.getValueAt(0, linha).toString();
            admSelecionado = controle.findByNumRegistro(numregistro);
            AdministradorForm tela = new AdministradorForm(null,true);
            tela.setAdministrador(admSelecionado);
            tela.setVisible(true);
            //this.dispose();
        }else{
            JOptionPane.showMessageDialog(null, "selecione um administrador!");
        }
    }//GEN-LAST:event_PAlterarMouseClicked

    private void PExcluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_PExcluirMouseClicked
        // TODO add your handling code here:
        int linha = tAdministrador.getSelectedRow();
        if(linha != -1){
            String numeroRegistro = tAdministrador.getValueAt(0, linha).toString();
            ControleAdministrador controle = new ControleAdministrador();        
            Administrador a = controle.findByNumRegistro(numeroRegistro);
            if(a.getNumeroRegistro().equals(this.ancora.getNumeroRegistro())){
                JOptionPane.showMessageDialog(null, "Você não pode excluir a si mesmo!");
                return;
            }else{
                a.setStatus(false);
                try {
                    controle.alterar(a);
                    JOptionPane.showMessageDialog(null, "Administrador excluído com sucesso!");
                    botPesquisarMouseClicked(evt);
                } catch (Exception ex) {
                    Logger.getLogger(RecursosHumanosMotorista.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showMessageDialog(null, "Erro ao excluir o administrador!");
                }     
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Selecione um administrador");
        }
    }//GEN-LAST:event_PExcluirMouseClicked

