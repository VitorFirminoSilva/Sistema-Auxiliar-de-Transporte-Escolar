    private void pPagamentoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_pPagamentoMouseClicked
        // TODO add your handling code here:
        List<Funcionario> list;
        ControlePagamento controlePagamento = new ControlePagamento();
        ControleFuncionario controleFuncionario = new ControleFuncionario();
        
        
        if(controlePagamento.findByData(new Date()).size() > 0){
            JOptionPane.showMessageDialog(this, "Você não pode fazer isso hoje!");
            this.dispose();
            return;
        }
        
        try{
            list = controleFuncionario.findAll();
            for(Funcionario f: list){
                Pagamento pagamento = new Pagamento();
                pagamento.setData(new Date());
                pagamento.setFuncionario(f);
                pagamento.setValor(f.getSalarioBase());
                pagamento.setStatus("PG");
                f.addPagamentos(pagamento);
                controleFuncionario.alterar(f);
            }
            JOptionPane.showMessageDialog(this, "O pagamento dos funcionários foi liberado!");
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Aconteceu um erro ao pagar os funcionários!");
        }
    }//GEN-LAST:event_pPagamentoMouseClicked

