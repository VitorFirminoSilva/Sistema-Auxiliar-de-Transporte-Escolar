    private void pesquisaFuncionario(){
        ControleFuncionario controle = new ControleFuncionario();
        List<Funcionario> list = new ArrayList<>();
        DefaultTableModel model = (DefaultTableModel) tPagamento.getModel();
        model.setNumRows(0);
        list = controle.findAll();
        for(Funcionario f: list){
            model.addRow(new Object[] {f.getNumeroRegistro(), f.getNomeFuncionario(), f.getSalarioBase()});
        }
    }
    
